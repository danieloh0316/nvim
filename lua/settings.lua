local opt = vim.opt

opt.mouse = 'a'          -- enable mouse
opt.number = true        -- show line numbers
opt.expandtab = true     -- convert tabs to spaces
opt.shiftwidth = 4       -- number of spaces for each indentation
opt.tabstop = 4          -- number of spaces for a tab
opt.signcolumn = 'no'    -- hide sign column
opt.linebreak = true     -- don't wrap words to next line
opt.showmode = false     -- don't show mode in status line
opt.showcmd = false      -- don't show key presses in status line
opt.termguicolors = true -- enable true color
